package week6.day4;

public class LearnSwitch {

	public static void main(String[] args) {
		int day = 1;
		switch(day) {
		case 1:
			System.out.println("Monday");
		break;
		case 2:
			System.out.println("TuesDay");
		break;
		case 3:
			System.out.println("Wednesday");
		break;
		default:
			System.out.println("Select valid number");
		
		
		}

	}

}
